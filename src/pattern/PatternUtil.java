package pattern;

public class PatternUtil {

    public static void printSpace(int n){
        for(int i=0;i<n;i++){
            System.out.print(" "+" ");
        }
    }
    public static void printStar(int n){
        for (int i=0;i<n;i++){
            System.out.print("*"+" ");
        }
    }
}
